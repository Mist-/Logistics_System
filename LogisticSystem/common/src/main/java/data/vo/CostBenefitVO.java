package data.vo;

public class CostBenefitVO {


    double allIncome;
    double allPay;
    double allProfit;

    public CostBenefitVO() {

    }

    public double getAllIncome() {
        return allIncome;
    }

    public void setAllIncome(double allIncome) {
        this.allIncome = allIncome;
    }

    public double getAllPay() {
        return allPay;
    }

    public void setAllPay(double allPay) {
        this.allPay = allPay;
    }

    public double getAllProfit() {
        return allProfit;
    }

    public void setAllProfit(double allProfit) {
        this.allProfit = allProfit;
    }
}
